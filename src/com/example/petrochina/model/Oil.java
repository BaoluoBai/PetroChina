package com.example.petrochina.model;

public class Oil {	
	private String unit_price;
	
	private String gun_number;
	
	private String oil_kind;

	public String getUnit_price() {
		return unit_price;
	}

	public void setUnit_price(String unit_price) {
		this.unit_price = unit_price;
	}

	public String getGun_number() {
		return gun_number;
	}

	public void setGun_number(String gun_number) {
		this.gun_number = gun_number;
	}

	public String getOil_kind() {
		return oil_kind;
	}

	public void setOil_kind(String oil_kind) {
		this.oil_kind = oil_kind;
	}
	
}
